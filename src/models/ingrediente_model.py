class Ingrediente: 
    def __init__(self, id_ingrediente, id_marca, nombre_ingrediente, fecha_compra_ingrediente, fecha_vencimiento_ingrediente, estado_rg, fecha_rg, fecha_md) -> None:
        self.id_ingrediente = id_ingrediente
        self.id_marca = id_marca
        self.nombre_ingrediente = nombre_ingrediente
        self.fecha_compra_ingrediente = fecha_compra_ingrediente
        self.fecha_vencimiento_ingrediente = fecha_vencimiento_ingrediente
        self.estado_rg = estado_rg
        self.fecha_rg = fecha_rg
        self.fecha_md = fecha_md
